export function normalize(s: string) {
  return s.toLowerCase().trim();
}

export function theoryMatchesSearch(t: Theory, term: string) {
  const q = normalize(term);
  if (!q) return true;
  const haystack = [
    t.name,
    t.category,
    t.hub,
    t.unit,
    t.chapter,
    t.level,
    t.summary,
    ...t.theorists,
    ...t.assumptions,
    ...t.concepts,
    ...t.useWhen,
    ...t.avoidWhen,
    ...t.strengths,
    ...t.limitations,
    ...t.comparisons,
    ...t.caseClues,
    t.workplaceExample,
    t.southAfricanExample,
    t.paragraphTemplate,
    ...t.recommendations,
    t.sourceStatus,
  ]
    .filter(Boolean)
    .join(" ")
    .toLowerCase();
  return haystack.includes(q);
}
